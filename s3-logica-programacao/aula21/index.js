/**
 * Operadores de Comparação:
 * > Maior que
 * >= Maior igual que
 * < Menor que
 * <= Menor igual que
 * == igualdade (valor)
 * === igualdade estrita (valor e tipo)
 * != diferente de (valor)
 * !== diferente estrito (valor e tipo)
 */

console.log(10 > 5);
console.log(10 == "10"); //Comparação de String com Number
console.log(10 === "10"); // Comparação de String com Number
console.log(10 !== "10");
console.log(10 != "10");